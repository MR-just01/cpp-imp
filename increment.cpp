#include<iostream>
 using namespace std;
 int main(){
     int n;
     cout<<"Enter the number:";
     cin>>n ;
     cout<<" the original value of n :"<< n <<endl;
      cout<<"++n : "<<++n<<endl;
      cout<<"n++ : "<<n++<<endl;
      cout <<" the value of n after increment:"<<n<<endl;
      return 0 ;
 }


 